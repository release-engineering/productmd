class TestVerifyAfterLocalizeIntegration:
    """Test verify after localize with computed checksums."""

    def test_verify_localized_compose_with_checksums(self):
        """Upgrade with checksums, then verify the compose passes."""
        with tempfile.TemporaryDirectory() as tmp_dir:
            # Download full compose (metadata + artifacts)
            compose_dir = os.path.join(tmp_dir, "compose")
            _download_compose(compose_dir)

            # Step 1: Upgrade with --compute-checksums
            v2_dir = os.path.join(tmp_dir, "v2")
            result = _run_productmd(
                "upgrade",
                "--output",
                v2_dir,
                "--base-url",
                f"{HTTP_BASE_URL}/",
                "--compute-checksums",
                compose_dir,
            )
            assert result.returncode == 0, f"stdout: {result.stdout}\nstderr: {result.stderr}"

            # Step 2: Copy the checksummed v2.0 metadata into the compose
            # so verify can find both the metadata and the artifacts.
            import shutil

            metadata_dir = os.path.join(compose_dir, "metadata")
            for fname in os.listdir(v2_dir):
                if fname.endswith(".json"):
                    shutil.copy2(
                        os.path.join(v2_dir, fname),
                        os.path.join(metadata_dir, fname),
                    )

            # Step 3: Verify — checksums computed from the same files
            result = _run_productmd(
                "verify",
                compose_dir,
            )

            assert result.returncode == 0, f"stdout: {result.stdout}\nstderr: {result.stderr}"
            assert "Verified:" in result.stdout


