class TestRoundTripIntegration:
    """Test full round-trip: v1.2 -> v2.0 -> localize -> downgrade -> compare."""

    def test_full_round_trip(self):
        """Full round-trip preserves compose structure."""
        with tempfile.TemporaryDirectory() as tmp_dir:
            compose_dir = os.path.join(tmp_dir, "compose")
            _download_metadata(compose_dir)

            v2_dir = os.path.join(tmp_dir, "v2")
            local_dir = os.path.join(tmp_dir, "local")
            v1_dir = os.path.join(tmp_dir, "v1-downgraded")

            # Step 1: Upgrade to v2.0
            result = _run_productmd(
                "upgrade",
                "--output",
                v2_dir,
                "--base-url",
                f"{HTTP_BASE_URL}/",
                os.path.join(compose_dir, "metadata", "images.json"),
            )
            assert result.returncode == 0

            # Step 2: Localize (download)
            result = _run_productmd(
                "localize",
                "--output",
                local_dir,
                "--parallel-downloads",
                "2",
                "--no-verify-checksums",
                "--retries",
                "1",
                os.path.join(v2_dir, "images.json"),
            )
            assert result.returncode == 0

            # Step 3: Downgrade back to v1.2
            localized_compose = os.path.join(local_dir, "compose")
            result = _run_productmd(
                "downgrade",
                "--output",
                v1_dir,
                localized_compose,
            )
            assert result.returncode == 0

            # Step 4: Verify v1.2 output has correct paths
            with open(os.path.join(v1_dir, "images.json")) as f:
                data = json.load(f)

            assert data["header"]["version"] == "1.2"

            images = data["payload"]["images"]
            for variant in images:
                for arch in images[variant]:
                    for img in images[variant][arch]:
                        assert "path" in img
                        assert "location" not in img
                        assert not img["path"].startswith("http")


