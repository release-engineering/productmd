"""
Integration tests for productmd CLI tools.

These tests run inside a container with access to:
  - httpserver: serves a v1.2 compose at HTTP_BASE_URL
  - registry: OCI registry at REGISTRY_URL (seeded with test artifacts)

Run via:  ./tests/integration/run.sh

These tests are NOT discovered by unit test runners (tox/pytest) since
they live in tests/integration/ and require the Docker Compose services.

All CLI inputs must be local files or directories. Tests download
metadata from the HTTP server to local temp directories first, then
pass local paths to the CLI commands.
"""

import json
import os
import subprocess
import tempfile
import urllib.request


HTTP_BASE_URL = os.environ.get("HTTP_BASE_URL", "http://localhost:8080")
REGISTRY_URL = os.environ.get("REGISTRY_URL", "localhost:5000")

# Metadata files available on the HTTP server
METADATA_FILES = ["composeinfo.json", "images.json", "rpms.json", "extra_files.json"]

# v2.0 OCI metadata files generated by registry-seed with real digests.
# Served at {HTTP_BASE_URL}/oci-metadata/<filename>
OCI_METADATA_FILES = ["images-oci.json", "images-mixed.json", "rpms-oci.json", "extra_files-oci.json"]


def _run_productmd(*args):
    """Run a productmd CLI command and return the CompletedProcess."""
    return subprocess.run(
        ["productmd"] + list(args),
        capture_output=True,
        text=True,
    )


def _download_metadata(dest_dir):
    """Download metadata files from the HTTP server to a local directory.

    Creates a compose-like layout: dest_dir/metadata/*.json
    """
    metadata_dir = os.path.join(dest_dir, "metadata")
    os.makedirs(metadata_dir, exist_ok=True)

    for filename in METADATA_FILES:
        url = f"{HTTP_BASE_URL}/metadata/{filename}"
        dest = os.path.join(metadata_dir, filename)
        try:
            urllib.request.urlretrieve(url, dest)
        except urllib.error.URLError:
            pass  # Some files may not exist (e.g., modules.json)

    return dest_dir


ARTIFACT_PATHS = [
    "Server/x86_64/iso/boot.iso",
    "Server/x86_64/os/GPL",
    "Server/x86_64/os/Packages/b/bash-5.2.26-3.fc41.x86_64.rpm",
    "Server/aarch64/iso/boot.iso",
    "Server/aarch64/os/GPL",
    "Server/aarch64/os/Packages/b/bash-5.2.26-3.fc41.aarch64.rpm",
]


def _download_compose(dest_dir):
    """Download the full compose (metadata + artifacts) from the HTTP server.

    Creates a proper compose layout::

        dest_dir/
          metadata/
            composeinfo.json
            images.json
            rpms.json
            extra_files.json
          Server/
            x86_64/
              iso/boot.iso
              os/...
            aarch64/
              iso/boot.iso
              os/...
    """
    _download_metadata(dest_dir)

    for rel_path in ARTIFACT_PATHS:
        url = f"{HTTP_BASE_URL}/{rel_path}"
        dest = os.path.join(dest_dir, rel_path)
        os.makedirs(os.path.dirname(dest), exist_ok=True)
        try:
            urllib.request.urlretrieve(url, dest)
        except urllib.error.URLError:
            pass

    return dest_dir


def _download_oci_metadata(dest_dir, files=None):
    """Download generated v2.0 OCI metadata from the HTTP server.

    These files are generated by the registry-seed container with real
    OCI digests and served at /oci-metadata/*.json by the HTTP server.

    Downloads to dest_dir/<filename> (flat, not in a metadata/ subdir).
    """
    os.makedirs(dest_dir, exist_ok=True)
    for filename in files or OCI_METADATA_FILES:
        url = f"{HTTP_BASE_URL}/oci-metadata/{filename}"
        dest = os.path.join(dest_dir, filename)
        urllib.request.urlretrieve(url, dest)
    return dest_dir


class TestUpgradeIntegration:
    """Test upgrading a v1.2 compose to v2.0 via the CLI."""

    def test_upgrade_compose_dir(self):
        """Upgrade a v1.2 compose directory to v2.0."""
        with tempfile.TemporaryDirectory() as tmp_dir:
            compose_dir = os.path.join(tmp_dir, "compose")
            _download_metadata(compose_dir)
            output_dir = os.path.join(tmp_dir, "v2")

            result = _run_productmd(
                "upgrade",
                "--output",
                output_dir,
                "--base-url",
                f"{HTTP_BASE_URL}/",
                compose_dir,
            )

            assert result.returncode == 0, f"stdout: {result.stdout}\nstderr: {result.stderr}"

            # Verify v2.0 output exists
            images_path = os.path.join(output_dir, "images.json")
            assert os.path.isfile(images_path)

            with open(images_path) as f:
                data = json.load(f)
            assert data["header"]["version"] == "2.0"

            # URLs should point to HTTP server
            for variant in data["payload"]["images"]:
                for arch in data["payload"]["images"][variant]:
                    for img in data["payload"]["images"][variant][arch]:
                        assert img["location"]["url"].startswith(HTTP_BASE_URL)

